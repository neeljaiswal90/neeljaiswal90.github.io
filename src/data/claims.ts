export const claimById = new Map<string, ClaimRecord>(claims.map((claim) => [claim.id, claim]));

export function getClaim(id: string): ClaimRecord {
  const claim = claimById.get(id);
  if (!claim) throw new Error(`Unknown claim ID: ${id}`);
  return claim;
}

export function getPublishableClaim(id: string): ClaimRecord {
  const claim = getClaim(id);
  if (claim.publishingStatus !== 'published' && claim.publishingStatus !== 'reference-only') {
    throw new Error(`Claim is not approved for production: ${id}`);
  }
  return claim;
}
